package basico;
public class Variaveis {
       public static void main(String[] args) {
        // Tipo nome = conteudo;
        String meuNome = "Carlos Henrique";
        int idade = 21;
        //double altura = 1.72;
        //char sexo = 'M';
        //boolean casado = false;

        // Sobrepor o conteúdo da variável

        meuNome = " Carlinhos";

        System.out.println("Meu nome é : " + meuNome );

        // Somar conteúdo de uma variável
        meuNome += " Carlinhos";

        System.out.println("Meu nome é : " + meuNome + " e minha idade é: " + idade);
        }
    }

